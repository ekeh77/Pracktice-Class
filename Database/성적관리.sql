--2. 과목테이블 생성
CREATE TABLE SUBJECTS(
    SUBID VARCHAR2(20) NOT NULL,
    SUBNAME VARCHAR2(30) NOT NULL
);

--과목 데이터 생성
INSERT INTO SUBJECTS VALUES('SUB001', '국어');
INSERT INTO SUBJECTS VALUES('SUB002', '수학');

SELECT * FROM SUBJECTS;

--시험을 친다
CREATE TABLE EXAMS(
    SID VARCHAR2(20) NOT NULL,
    SUBID VARCHAR2(20) NOT NULL,
    SCORE NUMBER(7) NOT NULL
);

SELECT * FROM STUDENTS; -- 엔터티 - 객체 - 절대로 중복될 수 없다. - SID
