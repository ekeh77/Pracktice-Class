create or replace NONEDITIONABLE TRIGGER TRG_INS_POINT_CUS 
AFTER INSERT ON POINT_TBL 
REFERENCING OLD AS O NEW AS N 
FOR EACH ROW 

BEGIN
 
   IF INSERTING THEN
   
     UPDATE CUSTOMERS_TBL
     SET CT_POINT = CT_POINT + :N.PT_SCORE 
     WHERE :N.PT_SAVE_USE = 'S'
     AND CT_ID = :N.CT_ID 
     ;
     
     UPDATE CUSTOMERS_TBL
     SET CT_POINT = CT_POINT - :N.PT_SCORE 
     WHERE :N.PT_SAVE_USE = 'U'   
     AND CT_ID = :N.CT_ID
     ;
   
   END IF;
END;