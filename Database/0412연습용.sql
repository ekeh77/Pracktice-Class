-- 6. 가장 많이 성장한 나무의 '과'를 구하세요.

SELECT TFAMILY, GROW FROM
(
SELECT T2.TFAMILY,(CK_LENGTH - PT_LENGTH) AS GROW
FROM CHECK_TREE T1, TREE T2, PLANT_TREE T3
WHERE T1.PTID = T3.PLTID
AND T2.TID = T3.TID
GROUP BY T2.TFAMILY, (CK_LENGTH - PT_LENGTH)
ORDER BY (CK_LENGTH - PT_LENGTH) DESC
)
WHERE ROWNUM = 1
;


-- 7. 가장 적게 성장한 나무가 심긴 공원을 구하세요.

SELECT PKNAME, ROWTREE FROM(
SELECT T2.PKNAME,(CK_LENGTH - PT_LENGTH) ROWTREE
FROM CHECK_TREE T1, PARK T2, PLANT_TREE T3
WHERE T1.PTID = T3.PLTID
AND T2.PKID = T3.PKID
GROUP BY T2.PKNAME, (CK_LENGTH - PT_LENGTH)
ORDER BY (CK_LENGTH - PT_LENGTH)ASC
)
WHERE ROWNUM = 1
;

-- 8. '과' 별로 점검된 나무의 평균 성장률을 구하세요.

SELECT T2.TFAMILY,TO_CHAR(AVG((CK_LENGTH - PT_LENGTH)/PT_LENGTH),'0.00') AS AVGGROW 
FROM CHECK_TREE T1, TREE T2, PLANT_TREE T3
WHERE T1.PTID = T3.PLTID
AND T2.TID = T3.TID
GROUP BY T2.TFAMILY
;

