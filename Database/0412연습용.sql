GROUP BY T2.PKNAME 
;

-- 2. '지리산공원', '남한산성공원'에 심긴 나무를 '과'를 따라 분류하세요.

SELECT T1.TFAMILY, T1.TNAME
FROM TREE T1, PARK T2, PLANT_TREE T3
WHERE T1.TID = T3.TID
AND T2.PKID = T3.PKID
AND (T2.PKNAME = '지리산공원' OR T2.PKNAME = '남한산성공원')
GROUP BY T1.TFAMILY, T1.TNAME
;

-- 3. 공원의 분류에 따라 심긴 나무의 수를 구하세요.

SELECT T2.PKNAME, COUNT(*)
FROM TREE T1, PARK T2, PLANT_TREE T3
WHERE T1.TID = T3.TID
AND T2.PKID = T3.PKID
GROUP BY T2.PKNAME
;

-- 4. 공원 분류에 따른 점검률을 구하세요.

SELECT PLT_PKNAME, TO_CHAR(100+((CHK_TREE - PLT_TREE)/PLT_TREE*100),'00.00')||'%' AS CHKPER  FROM
(
SELECT T2.PKCLASS AS PLT_PKNAME, COUNT(*) AS PLT_TREE
FROM PLANT_TREE T1, PARK T2, CHECK_TREE T3
WHERE T1.PKID = T2.PKID
AND T1.PLTID = T3.PTID(+)
GROUP BY T2.PKCLASS
) T1,
(
SELECT T2.PKCLASS AS CHK_PKNAME, COUNT(*) AS CHK_TREE
FROM PLANT_TREE T1, PARK T2, CHECK_TREE T3
WHERE T1.PKID = T2.PKID
AND T1.PLTID = T3.PTID
GROUP BY T2.PKCLASS
) T2
WHERE T1.PLT_PKNAME = T2.CHK_PKNAME
;

