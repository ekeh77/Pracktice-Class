package PKG_Cal;

//객체지향언어에서의 class -> 객체
public class Calculator {
	
	//속성값과 메서드
	
	//method
	public int setPlus(int a, int b) {
		
		int c = a + b;
		
		return c;
	}
	
	public int setMinus(int a, int b) {
		
		int c = a - b;
		
		return c;
	}
	
	public int setCross(int a, int b) {
		
		int c = a * b;
		
		return c;
	}
	
	public float setDevide(float a, float b) {
		
		float c = a / b;
		
		return c;
	}
}
