    FROM
    (
    SELECT MENUID, LPAD(' ', 15*(LEVEL-1)) || MENUNAME AS MENUNAME, MENULVL,PARENTID,
    CONNECT_BY_ROOT MENUID AS ROOTID, -- 최상위값
    CONNECT_BY_ISLEAF --아들이 있으면 0 없으면 1
